package com.testgen.entity;

public class Position {
    private int positionid;//职位id
    private String positionname;//职位名称
    private String positioncontent;//职位责任
	public int getPositionid() {
		return positionid;
	}
	public void setPositionid(int positionid) {
		this.positionid = positionid;
	}
	public String getPositionname() {
		return positionname;
	}
	public void setPositionname(String positionname) {
		this.positionname = positionname;
	}
	public String getPositioncontent() {
		return positioncontent;
	}
	public void setPositioncontent(String positioncontent) {
		this.positioncontent = positioncontent;
	}
    
}
